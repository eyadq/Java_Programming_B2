package day03_comments_escape_sequence;

public class TabAndNextLine {

    public static void main(String [] args){

        //tab - \t
        System.out.println("This is not tabbed");
        System.out.println("\tThis is tabbed one time");
        System.out.println("\t\tThis is tabbed two times");
        System.out.println("    Four spaces equals one tab");

        // new line \n
        System.out.println("1) Go to the store\n2) grab milk\n3) pay cashier");
    }


}
