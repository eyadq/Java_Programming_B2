package day07_relational_operators;

public class OperatorPractice2 {

    public static void main(String[] args) {

        int a = 40;

        System.out.println(--a);  //pre-decrement, output 39
        System.out.println(--a);  //pre-decrement, output 38
        System.out.println(a--);  //post-decrement, output 38
        System.out.println(a--);  //post-decrement, output 38
        System.out.println(--a);  //pre-decrement, output 36
        System.out.println(--a);  //pre-decrement, output 35
        System.out.println(a--);  //post-decrement, output 35
        System.out.println(a); // 34


    }


}
